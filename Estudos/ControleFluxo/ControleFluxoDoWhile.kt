/* DO WHILE: Faça uma instrução enquanto uma condição é verdadeira.
* A diferença para o While sozinho, é que no While, antes de realizar a instrução, será verificada a condição;
* ao passo que no Do While, a instrução é executada antes de verificar a condição.
*
* Pode ser utilizadas as palavras break e continue
* fun nomeFunção(){
*   do {
*      Instrução
*   } while (condição)
*
*}
*Ex.:
*   fun main(){
*       var index = 0
*       do{
*           println("Olá! $index")
*           index++
*       } while (index < 10)
*   }
* */