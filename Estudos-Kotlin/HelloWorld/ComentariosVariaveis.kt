//Comentário do tipo linha única: Função Exercicios.main é um ponto de entrada, só pode ter uma por arquivo.
/* Comentário do tipo linhas multiplas:
No momento da compilação, os comentários são excluídos, então, não há
uma preocupação de que com comentários o código fica pesado */
fun main() {
    var teste: String = "Hello World!"
    println(teste)
    teste = "Olá, mundo!!"
    println(teste)
    val str: String = "Imutável"
    println(str)
  /*  str = "Será que muda?"
    println(str) */
}

//val vem de value, uma vez que recebe um valor, não muda mais = variável imutável = constante
//var vem de variabel, pode mudar o valor recebido = variável mutável